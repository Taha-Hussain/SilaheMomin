package net.mk786110.silahemomin.Model;

public class Dua {
    private String dua_name;
    private String dua_arabic_part;
    private String dua_urdu_part;



    public String getDua_name() {
        return dua_name;
    }

    public void setDua_name(String dua_name) {
        this.dua_name = dua_name;
    }

    public String getDua_arabic_part() {
        return dua_arabic_part;
    }

    public void setDua_arabic_part(String dua_arabic_part) {
        this.dua_arabic_part = dua_arabic_part;
    }

    public String getDua_urdu_part() {
        return dua_urdu_part;
    }

    public void setDua_urdu_part(String dua_urdu_part) {
        this.dua_urdu_part = dua_urdu_part;
    }
}
